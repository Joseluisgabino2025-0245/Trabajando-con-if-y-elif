#Número mayor entre dos

numr1 = float(input("Digite el primer número: "))
numr2 = float(input("Digite el segundo número: "))

if numr1 > numr2:
    print("El primer número es mayor.")
    
elif numr2 > numr1:
    print("El segundo número es mayor.")

else:
    print("Ambos números son iguales.")
